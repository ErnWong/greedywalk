function r = Reverse(arr)
% Project - Reverse
%
% r = Reverse(arr)
%
% Returns a reversed copy of the array arr.
%
% Example:
%     r = Reverse([1 3 2 2 5]);
%     % r = [5 2 2 3 1]
%
% Input:  arr = source array
% Output: r = reversed copy of array arr.
%
% Author: Ernest Wong (ewon746)
% Date: 2017-09-03

	r = arr(end:-1 :1);

end
